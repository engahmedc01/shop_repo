    boolean validateForm() {

        if (name_TF.getText().isEmpty()) {
            wraning_label.setText("يجب إدخال اسم العنوان");
            return false;

        } else if (notes_TA.getText().isEmpty()) {

            wraning_label.setText("يجب إدخال البيان");
            return false;
        } else if (amount_TF.getText().isEmpty()) {

            wraning_label.setText("يجب إدخال المبلغ");
            return false;
        } else if (!data_source.vallidateSafeWithdrawal(Double.parseDouble(amount_TF.getText()))) {
            return false;
        } else {
            wraning_label.setText("");
        }
        return true;

    }

    void inatiatePage() {
        this.name_TF.setText("");
        this.amount_TF.setText("");
        this.notes_TA.setText("");

        int SeasonID = data_source.getCurrentSeason();
        double Shoptotal = data_source.getKTypeWithdrawal("K_V", SeasonID, 1);

        this.ShopTotal_TF.setText(String.valueOf(Shoptotal));

        fillAllContractorsTable();

    }

    void fillAllContractorsTable() {
 int seasonID=data_source.getCurrentSeason();
        Vector<Object> data = data_source.getAllContractors("K_V",seasonID);
        DefaultTableModel dtm = (DefaultTableModel) allContractors_table.getModel();
        dtm.setDataVector(data, allcont_column_titles);

    }

    void searchContAccount(String Name) {
int seasonID=data_source.getCurrentSeason();
        Object[][] table = data_source.getContractorAccount(Name, "K_V",2,seasonID);
        DefaultTableModel dtm = (DefaultTableModel) varaiatiesAccount_table.getModel();
        dtm.setDataVector(table, contractor_columnsTitle);
        varaiatiesAccount_table.setModel(dtm);
        setLabourAccount_tableSize();

        double kPaid = data_source.getContractorWithadrawal(Name, 0,seasonID);
        double kshop = data_source.getContractorWithadrawal(Name, 1,seasonID);

    }

    void setLabourAccount_tableSize() {
        // LabourAccount_table.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        TableColumn column = null;
        for (int i = 0; i < varaiatiesAccount_table.getColumnModel().getColumnCount(); i++) {
            column = varaiatiesAccount_table.getColumnModel().getColumn(i);

            switch (i) {
                case 0:
                    column.setPreferredWidth(250);
                    break;
                case 1:
                    column.setPreferredWidth(80);
                    break;
                case 2:
                    column.setPreferredWidth(100);
                    break;

            }
        }

    }


    private void save_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save_btnActionPerformed
        // TODO add your handling code here:
        if (validateForm()) {
            String name = name_TF.getText();

            String type = "K_V";
            double amount = Double.parseDouble(amount_TF.getText());
            String report = notes_TA.getText();
            int paid = 1;
            java.util.Date date = date_DP.getDate();
            int season_id = data_source.getCurrentSeason();

            data_source.contractorTransaction(name, "", "", type, amount, report, myFrame.firdage_id, paid, new java.sql.Date(date.getTime()), season_id);
            inatiatePage();

        }
    }//GEN-LAST:event_save_btnActionPerformed

