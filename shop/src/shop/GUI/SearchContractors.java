    boolean ValidateForm() {

        java.util.Date date = null;
        if (fromDate_DP.getDate() == null) {
            this.warnibg_label.setText("يجب إدخال من تاريخ");
            return false;

        }
        if (toDate_DP.getDate() == null) {
            this.warnibg_label.setText("يجب إدخال إلي تاريخ");
            return false;

        }
        if (kind_CB.getSelectedIndex() == 0 && KL_TF.getText().isEmpty()) {

            this.warnibg_label.setText("يجب إدخال الأسم");
            return false;

        }
        if (kind_CB.getSelectedIndex() == 1 && KS_TF.getText().isEmpty()) {

            this.warnibg_label.setText("يجب إدخال الأسم");
            return false;

        }

        String name = (kind_CB.getSelectedIndex() == 0) ? KL_TF.getText() : KS_TF.getText();
        String type = (kind_CB.getSelectedIndex() == 0) ? "K_L" : "K_S";

        if ((data_source.getContId(name, type)) == 0) {
            this.warnibg_label.setText("هذا الأسم غير مسجل");
            return false;

        }
        warnibg_label.setText("");
        return true;
    }

    private void search_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_btnActionPerformed
        // TODO add your handling code here:
        if (ValidateForm()) {
            java.util.Date startDate = fromDate_DP.getDate();
            java.util.Date EndDate = toDate_DP.getDate();
            String name = (kind_CB.getSelectedIndex() == 0) ? KL_TF.getText() : KS_TF.getText();
            String type = (kind_CB.getSelectedIndex() == 0) ? "K_L" : "K_S";
            Vector<Object>data=data_source.searchForContractor(name, new java.sql.Date(startDate.getTime()), new java.sql.Date(EndDate.getTime()), type);
           double kPaid=data_source.KContractorAmount(name,  new java.sql.Date(startDate.getTime()), new java.sql.Date(EndDate.getTime()), type);
           double shopPaid=data_source.shopContractorAmount(name,  new java.sql.Date(startDate.getTime()), new java.sql.Date(EndDate.getTime()), type);

        DefaultTableModel dtm=(DefaultTableModel)  ChemicalSupplierAcccount_table4.getModel();
        dtm.setDataVector(data, contractor_columnsTitle);
        
         

            shopPaid_TF.setText(String.valueOf(shopPaid));
            KPaid_TF.setText(String.valueOf(kPaid));
            total_TF.setText(String.valueOf(kPaid+shopPaid));

        }
    }//GEN-LAST:event_search_btnActionPerformed


