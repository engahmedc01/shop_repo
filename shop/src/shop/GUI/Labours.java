    void inatiatePage() {
        this.name_TF.setText("");
        this.address_TF.setText("");
        this.phone_TF.setText("");
        this.amount_TF.setText("");
        this.notes_TA.setText("");

        int SeasonID = data_source.getCurrentSeason();
        double ktotal = data_source.getKTypeWithdrawal("K_L", SeasonID, 0);
        double Shoptotal = data_source.getKTypeWithdrawal("K_L", SeasonID, 1);

        this.ShopTotal_TF.setText(String.valueOf(Shoptotal));
        this.Ktotal_TF.setText(String.valueOf(ktotal));

        fillAllContractorsTable();

    }

    void fillAllContractorsTable() {
 int seasonID=data_source.getCurrentSeason();
        Vector<Object> data = data_source.getAllContractors("K_L",seasonID);
        DefaultTableModel dtm = (DefaultTableModel) allContractors_table.getModel();
        dtm.setDataVector(data, allcont_column_titles);

    }

    void searchContAccount(String Name) {
        int seasonID=data_source.getCurrentSeason();
     
         int sort=2;
    if(K_RB.isSelected()){sort=0;}
    else if(shop_RB.isSelected()){sort=1;}
        Object[][] table = data_source.getContractorAccount(Name, "K_L",sort,seasonID);
        DefaultTableModel dtm = (DefaultTableModel) LabourAccount_table.getModel();
        dtm.setDataVector(table, contractor_columnsTitle);
        LabourAccount_table.setModel(dtm);
        setLabourAccount_tableSize();

        double kPaid = data_source.getContractorWithadrawal(Name, 0,seasonID);
        double kshop = data_source.getContractorWithadrawal(Name, 1,seasonID);

        shopPaid_TF.setText(String.valueOf(kshop));
        KPaid_TF.setText(String.valueOf(kPaid));
    }
