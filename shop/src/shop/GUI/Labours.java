    void setLabourAccount_tableSize() {
        // LabourAccount_table.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        TableColumn column = null;
        for (int i = 0; i < LabourAccount_table.getColumnModel().getColumnCount(); i++) {
            column = LabourAccount_table.getColumnModel().getColumn(i);

            switch (i) {
                case 0:
                    column.setPreferredWidth(250);
                    break;
                case 1:
                    column.setPreferredWidth(30);
                    break;
                case 2:
                    column.setPreferredWidth(80);
                    break;
                case 3:
                    column.setPreferredWidth(270);
                    break;

            }
        }

    }

