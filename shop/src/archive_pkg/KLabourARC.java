    private void all_RBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_all_RBActionPerformed
        // TODO add your handling code here:
        if(all_RB.isSelected()){
            int selected=allContractors_table.getSelectedRow();
            if(selected>-1){
                DefaultTableModel dtm=(DefaultTableModel)allContractors_table.getModel();
                String   name=dtm.getValueAt(selected, 1).toString();
                searchContAccount(name);

            }

            else if(!labourName_TF.getText().isEmpty()){
                searchContAccount(labourName_TF.getText());

            }

        }
    }//GEN-LAST:event_all_RBActionPerformed

